package greedy_algorithm.activity_selection_problem;

public class Activity {

    public Integer start;
    public Integer end;

    public Activity(Integer start, Integer end) {
        this.start = start;
        this.end = end;
    }

    public Integer getStart() {
        return start;
    }

    public Integer getEnd() {
        return end;
    }
    
    
}
